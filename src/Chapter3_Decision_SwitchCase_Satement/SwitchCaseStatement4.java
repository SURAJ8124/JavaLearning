package Chapter3_Decision_SwitchCase_Satement;

public class SwitchCaseStatement4 {
    public static void main(String[] args){

        int employeeSalary=50000;
        switch (employeeSalary) {
            case 10000:
                System.out.println("you should develop yourself");
                break;

            case 20000:
                System.out.println("your salary is 20000 you should buy bike");
                break;

            case 30000:
                System.out.println("Your salary is good");
                break;

            case 40000:
                System.out.println("your salary is 40000");
                break;

            case 50000:
                System.out.println("Your salary is 50000");
                break;

        }
    }
}
